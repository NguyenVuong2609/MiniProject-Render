let num = parseInt(prompt("Nhập 1 số từ 0-9"));
switch (num) {
  case 0:
    console.log("Không");
    break;
  case 1:
    console.log("Một");
    break;
  case 2:
    console.log("Hai");
    break;
  case 3:
    console.log("Ba");
    break;
  case 4:
    console.log("Bốn");
    break;
  case 5:
    console.log("Năm");
    break;
  case 6:
    console.log("Sáu");
    break;
  case 7:
    console.log("Bảy");
    break;
  case 8:
    console.log("Tám");
    break;
  case 9:
    console.log("Chín");
    break;
}